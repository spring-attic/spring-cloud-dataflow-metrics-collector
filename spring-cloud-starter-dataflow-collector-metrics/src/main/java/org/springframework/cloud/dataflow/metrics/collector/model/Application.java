package org.springframework.cloud.dataflow.metrics.collector.model;

import java.util.LinkedList;
import java.util.List;

import com.fasterxml.jackson.annotation.JsonCreator;

/**
 * @author Vinicius Carvalho
 */
public class Application {
	private String name;

	@JsonCreator
	public Application(String name) {
		this.name = name;
	}

	private List<Instance> instances = new LinkedList<>();

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List<Instance> getInstances() {
		return instances;
	}

	public void setInstances(List<Instance> instances) {
		this.instances = instances;
	}

	public Double getIncomingRate(){
		return getInstances().stream().mapToDouble(Instance::getIncomingRate).sum();
	}

	public Double getOutgoingRate(){
		return getInstances().stream().mapToDouble(Instance::getOutgoingRate).sum();
	}

	@Override
	public boolean equals(Object o) {
		if (this == o) return true;
		if (o == null || getClass() != o.getClass()) return false;

		Application that = (Application) o;

		return name != null ? name.equals(that.name) : that.name == null;
	}

	@Override
	public int hashCode() {
		return name != null ? name.hashCode() : 0;
	}
}
